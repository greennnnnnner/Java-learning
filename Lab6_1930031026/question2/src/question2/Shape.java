package question2;

public class Shape {
	private double x;
	private double y;
	public Shape(double x, double y) {
		this.x = x;
		this.y = y;
	}
	public double getX() {
		return this.x;
	}
	public double getY() {
		return this.y;
	}
	public double area() {
		System.out.println("An unknown shape has an unknown area!");
		return -1.0;
	}
	public static void testShape() {
		Shape s = new Shape(2.0,3.0);
		System.out.println(s.getX()==2.0);
		System.out.println(s.getY()==3.0);
		System.out.println(s.area()==-1.0);
	}
}
